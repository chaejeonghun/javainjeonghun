package Today1001;

//은행 업무 클래스 설계도

public class Bank {
	// 현재 잔고 금액 필드
	private int balance = 0;

	// 입금기능
	public void deposit(int amount) {
		balance += amount;
	}

	public void withdraw(int amount) {
		if (balance - amount >= 0)
			balance -= amount;
	}

	public void currentBalance() {
		System.out.println("잔고 : " + balance);
	}
}
